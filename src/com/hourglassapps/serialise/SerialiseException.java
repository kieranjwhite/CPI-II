package com.hourglassapps.serialise;

public class SerialiseException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SerialiseException() {
		super();
	}

	public SerialiseException(String pMsg) {
		super(pMsg);
	}

	public SerialiseException(Throwable pEx) {
		super(pEx);
	}

}
